texto= "Hola bienvenido a python"

#Imprime el texto
print(texto)

#Imprime la tercera palabra
print(texto[3])

#Para imprimir desde la letra "b"(posicion 5) hasta el letra "o" (posicion 15)
print(texto[5:15])

#Imprime desde la posicion "b"(posicion 5) hasta la palabra final "python"
print(texto[5:])

#Imprime a la inversa, desde el ultimo al principio
print(texto[-6:])